public class StatementsWhitespacesAndIndentation {

    public static void main(String[] args) {

        int myVariable = 50; // Statement 1
        myVariable++; // Statement 2
        myVariable--; // Statement 3

        System.out.println("This is a test"); // Statement 4
        System.out.println("This is a test" +
                "This is another test"); // Statement 5
    }
}
